// Función para generar más datos de prueba
export function generateMockData(type, count = 20) {
  const data = [];
  const baseData = type === 'users' ? mockUsers : type === 'tasks' ? mockTasks : mockProducts;
  
  for (let i = 0; i < count; i++) {
    const baseItem = baseData[i % baseData.length];
    const newItem = {
      ...baseItem,
      id: baseItem.id + (i * baseData.length),
      created_at: new Date(Date.now() - Math.random() * 30 * 24 * 60 * 60 * 1000).toISOString(),
      updated_at: new Date(Date.now() - Math.random() * 7 * 24 * 60 * 60 * 1000).toISOString()
    };
    
    if (baseItem.name) {
      newItem.name = `${baseItem.name} ${i > 0 ? i + 1 : ''}`.trim();
    }
    if (baseItem.email) {
      newItem.email = `${baseItem.email.split('@')[0]}${i > 0 ? i + 1 : ''}@example.com`;
    }
    if (baseItem.title) {
      newItem.title = `${baseItem.title} ${i > 0 ? i + 1 : ''}`.trim();
    }
    
    data.push(newItem);
  }
  
  return data;
}

